// API: /api/ia/context
// Obtiene el contexto del usuario para la aplicación
// Mantiene compatibilidad con useCurrentUser hook

import { getAdminFirestore } from '@/lib/firebase/admin';
import { NextRequest, NextResponse } from 'next/server';

export const dynamic = 'force-dynamic';
export async function GET(request: NextRequest) {
  try {
    const { searchParams } = new URL(request.url);
    const userId = searchParams.get('userId');
    const light = searchParams.get('light') === 'true';

    if (!userId) {
      return NextResponse.json(
        { error: 'userId is required' },
        { status: 400 }
      );
    }

    const db = getAdminFirestore();

    // Obtener datos del usuario
    const userDoc = await db.collection('users').doc(userId).get();

    if (!userDoc.exists) {
      return NextResponse.json({ error: 'User not found' }, { status: 404 });
    }

    const userData = userDoc.data();

    // Usar un organization_id por defecto si no tiene (para evitar "Cargando..." en el header)
    const DEFAULT_ORG_ID = 'org_los_senores_del_agro';
    const organizationId = userData?.organization_id || DEFAULT_ORG_ID;

    if (!userData?.organization_id && userData?.rol !== 'super_admin') {
      console.warn(
        `[API /ia/context] User ${userId} has no organization_id, using default: ${DEFAULT_ORG_ID}`
      );
    }

    // Construir respuesta básica del usuario
    const user = {
      id: userDoc.id,
      email: userData?.email || '',
      rol: userData?.rol || 'operario',
      organization_id: organizationId,
      personnel_id: userData?.personnel_id || null,
      activo: userData?.activo !== false,
      modulos_habilitados: userData?.modulos_habilitados || [],
      created_at: userData?.created_at?.toDate?.() || new Date(),
      updated_at: userData?.updated_at?.toDate?.() || new Date(),
    };

    // Si es light, solo devolver datos básicos
    if (light) {
      return NextResponse.json({
        success: true,
        user,
        context: null, // Light mode no carga contexto completo
      });
    }

    // Cargar contexto completo si no es light
    let personnel = null;
    let position = null;
    let department = null;

    if (user.personnel_id) {
      const personnelDoc = await db
        .collection('personnel')
        .doc(user.personnel_id)
        .get();
      if (personnelDoc.exists) {
        const pData = personnelDoc.data();
        personnel = {
          id: personnelDoc.id,
          nombres: pData?.nombres || '',
          apellidos: pData?.apellidos || '',
          email: pData?.email || '',
          tipo: pData?.tipo || '',
        };

        // Cargar puesto si existe
        if (pData?.puesto) {
          const posDoc = await db
            .collection('positions')
            .doc(pData.puesto)
            .get();
          if (posDoc.exists) {
            position = { id: posDoc.id, nombre: posDoc.data()?.nombre || '' };
          }
        }

        // Cargar departamento si existe
        if (pData?.departamento) {
          const deptDoc = await db
            .collection('departments')
            .doc(pData.departamento)
            .get();
          if (deptDoc.exists) {
            department = {
              id: deptDoc.id,
              nombre: deptDoc.data()?.nombre || '',
            };
          }
        }
      }
    }

    return NextResponse.json({
      success: true,
      user,
      context: {
        personnel,
        position,
        department,
      },
    });
  } catch (error) {
    console.error('[API /ia/context] Error:', error);
    return NextResponse.json(
      {
        error: 'Error fetching user context',
        message: error instanceof Error ? error.message : 'Unknown error',
      },
      { status: 500 }
    );
  }
}
